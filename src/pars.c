#include "filo.h"

int	parsn(int n, int m, int c, int v)
{
	if (n < 1 || n > 200)
		return (0);
	if (m < 60 || m > 200)
		return (0);
	if (c < 60 || c > 200)
		return (0);
	if (v == -1)
		return (1);
	if (v < 0 || v > 2147483647)
		return (0);
	return (1);
}
